package JavaOperator.java;

public class CompundAssignment {
    public static void main(String[] args) {
        int a=5;
        int b=3;
        System.out.println(5/3);
        System.out.println("+= "+(a+=b));
        System.out.println("/= "+(a /=b));
        System.out.println("%= "+(a %=b));
    }
}
